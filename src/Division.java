
public class Division {
    private int div_Id;
    private String name;

    public Division() {
    }

    public Division(int div_Id, String name) {
        this.div_Id = div_Id;
        this.name = name;
    }

    public int getId() {
        return div_Id;
    }

    public String getName() {
        return name;
    }
}
